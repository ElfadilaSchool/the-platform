function updatePageTranslations() {
    // Ensure currentLanguage is synced
    if (typeof window !== 'undefined' && window.currentLanguage) {
        currentLanguage = window.currentLanguage;
    } else if (typeof localStorage !== 'undefined') {
        const savedLang = localStorage.getItem('language') || localStorage.getItem('lang');
        if (savedLang) {
            currentLanguage = savedLang;
            if (typeof window !== 'undefined') {
                window.currentLanguage = savedLang;
            }
        }
    }

    console.log('[translations.js] updatePageTranslations called, currentLanguage:', currentLanguage);
    console.log('[translations.js] translations object exists:', !!translations);
    console.log('[translations.js] translations.ar exists:', !!(translations && translations.ar));

    if (translations && translations.ar && currentLanguage === 'ar') {
        const testKeys = ['status.pending', 'tasks.title', 'common.all_status', 'tasks.daily', 'common.director', 'common.others', 'tasks.instructions'];
        console.log('[translations.js] Testing Arabic translations access:');
        testKeys.forEach(key => {
            const directAccess = translations.ar[key];
            const inOperator = key in translations.ar;
            console.log(`[translations.js] Key '${key}':`, {
                directAccess: directAccess || 'NOT FOUND',
                inOperator: inOperator,
                match: directAccess && inOperator ? '✓' : '✗'
            });
        });

        // Test if we can access via bracket notation
        const testKey = 'common.director';
        console.log('[translations.js] Direct bracket test:', {
            'translations["ar"]': !!translations['ar'],
            'translations["ar"]["common.director"]': translations['ar'] ? translations['ar']['common.director'] : 'N/A',
            'translations.ar["common.director"]': translations.ar ? translations.ar['common.director'] : 'N/A'
        });
    }

    // Update all elements with data-translate attribute
    const elements = document.querySelectorAll('[data-translate]');
    console.log('[translations.js] Found', elements.length, 'elements with data-translate');

    let updatedCount = 0;

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (!key) return;

        let translation = translate(key);

        // If translation failed or returned the key, try direct lookup
        if (!translation || translation === key) {
            // Try direct access as fallback
            if (translations && translations[currentLanguage] && translations[currentLanguage][key]) {
                console.log('[translations.js] Found translation via direct access:', key, '=', translations[currentLanguage][key]);
                translation = translations[currentLanguage][key];
            } else if (translations && translations['en'] && translations['en'][key]) {
                // Fallback to English
                console.warn('[translations.js] Key not found in', currentLanguage, 'falling back to English:', key);
                translation = translations['en'][key];
            } else {
                console.warn('[translations.js] Translation not found for key:', key, 'in language:', currentLanguage);
                return; // Skip to next element if not found
            }
        }

        updatedCount++;

        // Preserve HTML structure if element contains only text
        if (element.children.length === 0) {
            element.textContent = translation;
        } else {
            // For elements with children, try to preserve HTML structure
            // Check if element has nested HTML tags (like <strong>)
            const innerHTML = element.innerHTML.trim();
            const hasHTMLTags = /<[^>]+>/.test(innerHTML);

            if (hasHTMLTags) {
                // If it has HTML tags, try to replace only the main text content
                // Find the first significant text node
                const walker = document.createTreeWalker(
                    element,
                    NodeFilter.SHOW_TEXT,
                    null,
                    false
                );

                const textNodes = [];
                let node;
                while (node = walker.nextNode()) {
                    if (node.nodeValue.trim()) {
                        textNodes.push(node);
                    }
                }

                // If we have text nodes, update them
                if (textNodes.length > 0) {
                    // If translation contains HTML, use innerHTML to preserve formatting
                    if (translation.includes('<')) {
                        element.innerHTML = translation;
                    } else {
                        // Update all text nodes with the translation
                        textNodes.forEach((textNode, index) => {
                            if (index === 0) {
                                // Replace first text node with full translation
                                textNode.nodeValue = translation;
                            } else {
                                // Remove other text nodes (they might be part of original English text)
                                textNode.nodeValue = '';
                            }
                        });
                    }
                } else {
                    // No text nodes found, check if translation has HTML
                    if (translation.includes('<')) {
                        element.innerHTML = translation;
                    } else {
                        element.textContent = translation;
                    }
                }
            } else {
                // No HTML tags in original, check if translation has HTML
                if (translation.includes('<')) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        }
    });

    // Update placeholder attributes
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (key) {
            const translated = translate(key);
            if (translated && translated !== key) {
                element.setAttribute('placeholder', translated);
                updatedCount++;
            }
        }
    });

    // Update alt attributes
    document.querySelectorAll('[data-translate-alt]').forEach(element => {
        const key = element.getAttribute('data-translate-alt');
        if (key) {
            const translated = translate(key);
            if (translated && translated !== key) {
                element.setAttribute('alt', translated);
                updatedCount++;
            }
        }
    });

    console.log('[translations.js] Updated', updatedCount, 'elements total');

    // Update title attributes
    document.querySelectorAll('[data-translate-title]').forEach(element => {
        const key = element.getAttribute('data-translate-title');
        element.setAttribute('title', translate(key));
    });

    // Update page title (title tag)
    const titleElement = document.querySelector('title[data-translate]');
    if (titleElement) {
        const titleKey = titleElement.getAttribute('data-translate');
        if (titleKey) {
            const translatedTitle = translate(titleKey);
            if (translatedTitle && translatedTitle !== titleKey) {
                document.title = translatedTitle;
                titleElement.textContent = translatedTitle;
            }
        }
    }

    // Update aria-label attributes
    document.querySelectorAll('[data-translate-aria-label]').forEach(element => {
        const key = element.getAttribute('data-translate-aria-label');
        element.setAttribute('aria-label', translate(key));
    });

    // Update option elements with data-translate attribute
    document.querySelectorAll('option[data-translate]').forEach(option => {
        const key = option.getAttribute('data-translate');
        if (key) {
            const translated = translate(key);
            if (translated && translated !== key) {
                option.textContent = translated;
                updatedCount++;
            }
        }
    });

    // Handle file input "No file chosen" text (browser-generated, need custom handling)
    // Note: We can't directly translate browser-generated text, but we can add a custom display
    document.querySelectorAll('input[type="file"]').forEach(fileInput => {
        // Check if there's a next sibling that shows file status
        let statusElement = fileInput.nextElementSibling;
        if (!statusElement || !statusElement.classList.contains('file-status')) {
            // Create a status element if it doesn't exist
            statusElement = document.createElement('span');
            statusElement.className = 'file-status text-sm text-gray-500 mt-1 block';
            statusElement.setAttribute('data-translate', 'common.no_file_chosen');
            fileInput.parentNode.insertBefore(statusElement, fileInput.nextSibling);
        }
        if (statusElement && statusElement.hasAttribute('data-translate')) {
            const key = statusElement.getAttribute('data-translate');
            const translated = translate(key);
            if (translated && translated !== key && !fileInput.files || fileInput.files.length === 0) {
                statusElement.textContent = translated;
                updatedCount++;
            } else if (fileInput.files && fileInput.files.length > 0) {
                statusElement.textContent = fileInput.files[0].name;
            }
        }
    });

    console.log('[translations.js] Translation update complete. Total elements updated:', updatedCount);
}

