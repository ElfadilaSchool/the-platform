// Translation utility functions
// currentLanguage is initialized in the IIFE below - reference from window to avoid conflicts
// Don't declare it here - just reference it from window to avoid conflicts with main.js
var currentLanguage;

function setLanguage(lang) {
    console.log('[translations.js] setLanguage called with:', lang);
    currentLanguage = lang;
    // Also update window.currentLanguage for main.js and other scripts
    if (typeof window !== 'undefined') {
        window.currentLanguage = lang;
    }

    // Store in both localStorage keys for compatibility
    if (typeof localStorage !== 'undefined') {
        localStorage.setItem('language', lang);
        localStorage.setItem('lang', lang);
    }

    // Update HTML direction for RTL languages
    if (typeof document !== 'undefined') {
        if (lang === 'ar') {
            document.documentElement.setAttribute('dir', 'rtl');
            document.documentElement.setAttribute('lang', 'ar');
            if (document.body && document.body.classList) {
                document.body.classList.add('rtl');
            }

            // Load RTL CSS if not already loaded - skip if path doesn't exist to avoid errors
            if (document.head && !document.querySelector('link[href*="rtl.css"]')) {
                // Don't load RTL CSS dynamically - let it be handled by the page itself if needed
                // This avoids path issues across different file structures
            }
        } else {
            document.documentElement.setAttribute('dir', 'ltr');
            document.documentElement.setAttribute('lang', lang);
            if (document.body && document.body.classList) {
                document.body.classList.remove('rtl');
            }
        }
    }

    // Update translations immediately
    if (typeof updatePageTranslations === 'function') {
        console.log('[translations.js] Calling updatePageTranslations');
        updatePageTranslations();
    } else {
        console.error('[translations.js] updatePageTranslations is not a function!');
    }

    // Dispatch language changed event
    if (typeof window !== 'undefined' && typeof document !== 'undefined') {
        const event = new CustomEvent('languageChanged', { detail: { language: lang } });
        document.dispatchEvent(event);
        console.log('[translations.js] languageChanged event dispatched');
    }
}

function translate(key) {
    // Ensure currentLanguage is up to date
    if (typeof window !== 'undefined' && window.currentLanguage) {
        currentLanguage = window.currentLanguage;
    } else if (typeof localStorage !== 'undefined') {
        const savedLang = localStorage.getItem('language') || localStorage.getItem('lang');
        if (savedLang) {
            currentLanguage = savedLang;
            if (typeof window !== 'undefined') {
                window.currentLanguage = savedLang;
            }
        } else {
            currentLanguage = 'en';
        }
    }

    // Ensure we have a valid currentLanguage
    if (!currentLanguage) {
        currentLanguage = 'en';
    }

    // Check if translations object is accessible
    if (typeof translations === 'undefined') {
        console.error('[translate] translations object is UNDEFINED!');
        return key;
    }

    // Try to get translation - check multiple ways
    let translation = null;

    // Ensure translations object and currentLanguage object exist
    if (!translations || !currentLanguage) {
        console.error('[translate] Missing translations or currentLanguage:', {
            translations: !!translations,
            currentLanguage
        });
        return key;
    }

    // Try accessing with bracket notation first (more reliable)
    const langObj = translations[currentLanguage];
    if (langObj && typeof langObj === 'object' && key in langObj) {
        translation = langObj[key];
    }
    // Fallback: try English
    else if (translations['en'] && key in translations['en']) {
        translation = translations['en'][key];
    }
    // Final fallback: return key
    else {
        translation = key;

        // Enhanced debugging for Arabic
        if (currentLanguage === 'ar' && key.includes('.')) {
            const existsInEn = translations['en'] && (key in translations['en']);
            const existsInAr = translations['ar'] && (key in translations['ar']);

            if (existsInAr) {
                // Key exists but lookup failed - try direct access
                console.warn('[translate] Key exists in Arabic but lookup failed, trying direct access:', key);
                translation = translations['ar'][key];
            } else if (existsInEn && !existsInAr) {
                console.warn('[translate] Key exists in English but NOT in Arabic:', key);
            } else {
                console.warn('[translate] Key does not exist in ANY language:', key);
            }
        }
    }

    // If translation is still the key (not found), try to provide a fallback
    if (translation === key && key.includes('.')) {
        const parts = key.split('.');
        const lastPart = parts[parts.length - 1];
        // Convert camelCase or snake_case to readable text
        return lastPart.replace(/[_-]/g, ' ').replace(/([A-Z])/g, ' $1').trim()
            .split(' ').map(word => word.charAt(0).toUpperCase() + word.slice(1)).join(' ');
    }

    return translation;
}

